-- 2º questão: --
-- O comando puxa a percentual de comissão > 0.10 e, de ferias = 1, já que 1 = true --
SELECT percentual_comissao, de_ferias FROM tabela_de_vendedores WHERE percentual_comissao > 0.10 AND de_ferias = '1';

-- 3º questão: --
-- O CPF é algo único, e uma pessoa pode fazer muitas compras com o CPF, portanto, a chave Primaria não pode repetir --
SELECT * FROM notas_fiscais WHERE matricula = '00237' OR data_venda = '2015-01-12';

-- 4º questão: --
-- O comando seleciona o produto com o preço > 44 e quantidade > 98, puxando da tabela de produtos e da tabela codigo 1022450 --
SELECT codigo_do_produto, quantidade FROM itens_notas_fiscais WHERE preco > 44 AND quantidade > 98;
SELECT codigo_do_produto, quantidade WHERE codigo_do_produto = '1022450';

